全球经济增长趋缓：各国传统政策工具施展空间有限

　　4月，国际货币基金组织发布新一期《世界经济展望》，将今年全球经济增速预期降至3.3%。世界经济会进一步变坏吗？

　　“短期内并不会出现所谓的‘下一轮经济衰退’，但我们依然非常关注下行的风险。”林建海表示，2008年全球金融危机爆发以来，全球经济增长势头在趋缓。此情况下，宏观经济政策的当务之急是防止增长进一步减速。

 　　这一警示并非空穴来风。一方面，近期许多经济体增长率普遍不及预期，按购买力平价GDP计算，今年占全球经济总量70％的经济体预计增长不及去年；另一方面，各国货币、财政等传统政策工具的腾挪空间已经不多，这也意味着一旦经济下行加剧，全球的抗风险能力也将变得脆弱。

　　“货币政策的空间是有限的。历史上很多例子都已经证明了货币政策不会提供‘免费午餐’。”林建海分析认为，当前的低利率环境给债务展期和债务管理提供了一定空间，但这并不意味着货币政策能无限制支持财政赤字而不给经济带来不良后果。“用央行发行钞票来刺激经济增长，可能会带来短暂的好处，但从长期看，将会引发高通货膨胀、汇率贬值、实际工资降低，经济和生活水平倒退等一系列问题。”

　　同样的掣肘也存在于财政政策之中。2008年以来，许多国家都采取了积极的财政措施刺激经济增长。截至目前，许多国家债务中政府债占的比重已经很大，有的国家甚至超过GDP100%。“这些国家再次推出财政刺激方案可能面临更大难度，财政工具的空间也比较小。”林建海说。

　　在这种情况下，假如全球经济加速下滑该如何应对？林建海认为，各国除了要将政策重心转至防止经济增长下滑过快外，国际间也需要加强经济政策的协调。

　　“从全球范围来看，经济政策的协调是应是多方面全方位。除了短期内各国间宏观经济政策的协调，中长期经济增长面临的一些挑战，比如人口老龄化、气候变化、技术进步等因素都要考虑进来。关键是通过协调努力与举措，带动全球生产率的提高，促进中长期经济持续增长。”

 反全球化情绪成国际协同“拦路虎”：加征关税不能解决贸易结构失衡

　　在市场人士看来，国际政策协调的难度也在增加。近年来，在经济增长疲弱的背景下，双边贸易失衡的上升已经引发保护主义政策的抬头。这些负面情绪正让国际多边秩序遭到挑战，国际协同精神也有所下降。

　　“国际货币基金组织最近对贸易失衡做了一个比较详尽的分析。分析发现，国际贸易失衡的95%是由于宏观经济因素带来的，而不是关税壁垒。” 林建海认为，贸易失衡不是不公平的贸易做法引起的， 而是宏观经济的失衡带来的。“比如，过度的财政开支、过度宽松的货币政策，都可能带来贸易方面的失衡。” 

　　“贸易战中没有赢家，关税本身不能解决根本问题。关税的增加，不仅对中美的贸易投资，甚至对全球经济都带来损害。”林建海建议，解决问题还需要从根源上对症下药。 

　　答案就藏在加强国际协同为前提的宏观经济政策优化之中。林建海进一步表示，“我们分析的研究报告认为，从根本上来说，一定要在经济政策方面寻找原因，对症下药。假如财政开支过多，要缩减财政政策。货币政策如果扩张太快，就要缓慢。然后要进行结构性改革，提高产品竞争力和生产率，只有这样才能使贸易持续进行下去。”

探索国际协同方案：“一带一路”倡议下的全球发展联动

　　面对世界经济增长压力与逆全球化情绪挑战，国际协同可否还有作为空间？已经连续两届参加“一带一路”国际合作高峰论坛的林建海表示，  “一带一路”可以在诸多方面为不同区域甚至全球经济发展做贡献，特别是有助于参与国解决基础建设短缺问题，并加强参与国之间的贸易、投资、融资和人文交往。中国在基础建设方面的丰富经验对这些国家十分重要、适用。充分发挥 “一带一路”的潜力不仅将惠及参与国，还将给全球经济发展提供动力。

　　“现在，30%左右的全球经济增长来自于中国。‘一带一路’在为许多国家提供中国经验的同时，在投资、贸易、融资、人文交流等方面的互联互通也在增进国与国之间的协同关系，这一点非常重要。”林建海分析，中国在第二届“一带一路”国际合作高峰论坛上提出的一系列重大改革开放措施，将进一步为“一带一路”建设创造条件，提供保障。

 　　“本届论坛上，还提到了可持续性，这对于国际协同发展的下一步进程非常重要。”林建海解释，“一带一路”相关项目都很大，涉及多个领域与多方利益，只有确保融资、环境等各方面的可持续性，才能确保项目的持续性，并最终为各方带来实实在在的好处。

